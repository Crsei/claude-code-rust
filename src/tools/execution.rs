//! Single tool execution pipeline — `runToolUse()`.
//!
//! Corresponds to: TOOL_EXECUTION_STATE_MACHINE.md §3
//!
//! Pipeline stages:
//!   1. Tool lookup (by name, then by alias fallback)
//!   2. Abort check
//!   3. Input validation (schema + semantic)
//!   4. Pre-tool hooks (can modify input, override permission, stop)
//!   5. Permission check (hook override → rule engine → mode)
//!   6. Tool execution (call with progress callback)
//!   7. Post-tool hooks (success or failure path)
//!   8. Result assembly (tool_result block + new messages + size enforcement)

#![allow(unused)]


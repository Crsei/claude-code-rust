/// Find a tool by name, with alias fallback.
fn find_tool(name: &str, tools: &Tools) -> Option<Arc<dyn Tool>> {
    // Primary: exact name match
    if let Some(tool) = tools.iter().find(|t| t.name() == name) {
        return Some(Arc::clone(tool));
    }

    // Fallback: check user_facing_name
    if let Some(tool) = tools.iter().find(|t| t.user_facing_name(None) == name) {
        return Some(Arc::clone(tool));
    }

    None
}

/// Create an error result for early termination.
fn make_error_result(
    tool_use_id: &str,
    tool_name: &str,
    message: &str,
    started: Instant,
) -> ToolExecutionResult {
    ToolExecutionResult {
        tool_use_id: tool_use_id.to_string(),
        tool_name: tool_name.to_string(),
        result: ToolResult {
            data: Value::String(message.to_string()),
            new_messages: vec![],
        },
        is_error: true,
        new_messages: vec![],
        hook_stopped_continuation: false,
        duration_ms: started.elapsed().as_millis() as u64,
    }
}

/// Enforce tool result size limit by truncating if necessary.
fn enforce_result_size(data: Value, max_chars: usize) -> Value {
    match &data {
        Value::String(s) if s.len() > max_chars => {
            let head = &s[..max_chars / 2];
            let tail = &s[s.len() - max_chars / 4..];
            let omitted = s.len() - head.len() - tail.len();
            Value::String(format!(
                "{}\n\n[... {} characters omitted ...]\n\n{}",
                head, omitted, tail
            ))
        }
        _ => data,
    }
}


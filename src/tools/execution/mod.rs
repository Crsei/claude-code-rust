// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/// Result of a single tool execution through the full pipeline.
#[derive(Debug)]
pub struct ToolExecutionResult {
    /// The tool_use_id that triggered this execution.
    pub tool_use_id: String,
    /// The resolved tool name.
    pub tool_name: String,
    /// The tool result data.
    pub result: ToolResult,
    /// Whether the result is an error.
    pub is_error: bool,
    /// New messages generated by the tool (e.g., sub-agent conversations).
    pub new_messages: Vec<crate::types::message::Message>,
    /// Whether a post-tool hook stopped continuation.
    pub hook_stopped_continuation: bool,
    /// Execution duration in milliseconds.
    pub duration_ms: u64,
}

/// Stage at which the execution stopped (for diagnostics).
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ExecutionStage {
    Lookup,
    AbortCheck,
    InputValidation,
    SemanticValidation,
    PreHooks,
    PermissionCheck,
    Executing,
    PostHooks,
    Completed,
}

// ---------------------------------------------------------------------------
// Internal helper used by both pipeline and security
// ---------------------------------------------------------------------------

/// Create an error result for early termination.
fn make_error_result(
    tool_use_id: &str,
    tool_name: &str,
    message: &str,
    started: Instant,
) -> ToolExecutionResult {
    ToolExecutionResult {
        tool_use_id: tool_use_id.to_string(),
        tool_name: tool_name.to_string(),
        result: ToolResult {
            data: Value::String(message.to_string()),
            new_messages: vec![],
        },
        is_error: true,
        new_messages: vec![],
        hook_stopped_continuation: false,
        duration_ms: started.elapsed().as_millis() as u64,
    }
}
